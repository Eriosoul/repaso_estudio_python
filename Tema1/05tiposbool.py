# tipos bool (boolean)
miVariable = False
print(miVariable)

# un valor es mayor que otro
miVariable = 3 > 2  # True
print(miVariable)
if miVariable:
    print(" El resultado fue verdadelo ")
else:
    print(" El resultado fue falso")

miVariable = 1 > 2  # False
print(miVariable)
if miVariable:
    print(" El resultado fue verdadelo ")
else:
    print(" El resultado fue falso")
