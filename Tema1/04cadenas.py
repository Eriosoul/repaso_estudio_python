# Cadena (String)
miGrupoFavorito: str = "sum 41" + " " + "Blink182"
comentario: str = "muy buenas bandas"
# concatenamos el texto con la variable
print("Mi grupo favorito es: " + miGrupoFavorito + " " + comentario)
# usando la coma se puede añadir las variables y nos añadien directamente los espacios
print("Mi grupo favorito es:", miGrupoFavorito, comentario)

# literales o int
numbero1 = "1"
numbero2 = "2"
print("Concatenación:", numbero1 + numbero2)
print("Suma:", int(numbero1) + int(numbero2))
