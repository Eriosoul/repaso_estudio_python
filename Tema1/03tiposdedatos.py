# tenemos diversos tipos de datos como puede ser
# Numeric puede ser integer, float, complex number
# integer
x: int = 10
print(x)
print(type(x)) # nos indicara a que tipo pertenece <class 'int'>
# float
x: float = 10.5
print(x)
print(type(x)) # nos indicara a que tipo pertenece <class 'float'>

# Dictionary
# Boolean
x = True
print(x)
print(type(x)) # nos indicara a que tipo pertenece <class 'bool'>
x = False
print(x)
print(type(x)) # nos indicara a que tipo pertenece <class 'bool'>
# set
# string, list, tuple
x: str = 'Hola'
print(x)
print(type(x)) # nos indicara a que tipo pertenece <class 'str'>