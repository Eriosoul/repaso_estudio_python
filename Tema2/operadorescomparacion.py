a = 4
b = 2
# preguntamos si son iguales
resultado = a == b
print(f'Resultado == : {resultado}')
# diferente a
resultado = a != b
print(f'Resultado != : {resultado}')
# menor que
resultado = a < b
print(f'Resultado < : {resultado}')
# menor o igual
resultado = a <= b
print(f'Resultado < : {resultado}')
# mayor que
resultado = a > b
print(f'Resultado > : {resultado}')
# mayor o igual
resultado = a >= b
print(f'Resultado > : {resultado}')